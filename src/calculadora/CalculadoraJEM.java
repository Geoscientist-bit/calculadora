    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        double segundoNumero;
        if (operacion == "nula") {
            lbResultado.setText(cadenaNumeros);
        }
        else if (operacion == "sumar") {
            segundoNumero = Double.parseDouble(cadenaNumeros);
            resultado = primerNumero + segundoNumero;
            lbResultado.setText(String.format("% .2f", resultado));
            cadenaNumeros = String.valueOf(resultado);
            operacion = "nula";
        }
        else if (operacion == "restar") {
            segundoNumero = Double.parseDouble(cadenaNumeros);
            resultado = primerNumero - segundoNumero;
            lbResultado.setText(String.format("% .2f", resultado));
            cadenaNumeros = String.valueOf(resultado);
            operacion = "nula";
        }
        else if (operacion == "multiplicar") {
            segundoNumero = Double.parseDouble(cadenaNumeros);
            resultado = primerNumero * segundoNumero;
            lbResultado.setText(String.format("% .2f", resultado));
            cadenaNumeros = String.valueOf(resultado);
            operacion = "nula";
        }
        else if (operacion == "dividir") {
            segundoNumero = Double.parseDouble(cadenaNumeros);
            if (segundoNumero == 0) {
                lbResultado.setText("No se puede dividir por cero");
            }
            else {
                resultado = primerNumero / segundoNumero;
                lbResultado.setText(String.format("% .2f", resultado));
                cadenaNumeros = String.valueOf(resultado);
                operacion = "nula";
            }
        }
        else if(operacion == "potencia"){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            resultado = Math.pow(primerNumero, segundoNumero);
            lbResultado.setText(String.format("% .2f", resultado));
            cadenaNumeros = String.valueOf(resultado);
            operacion = "nula";
            
            
        }
        else if(operacion == "porcentaje"){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            resultado = primerNumero * (segundoNumero / 100);
            lbResultado.setText(String.format("% .2f", resultado));
            operacion = "nula";
        }
        lbMostrarOperaciones.setText("");
        activado = true;
    }//GEN-LAST:event_btnIgualActionPerformed

